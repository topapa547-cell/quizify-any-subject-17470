// Get daily puzzle based on date
export const getDailyPuzzle = (): ChessPuzzle => {
  const today = new Date();
  const dayOfYear = Math.floor((today.getTime() - new Date(today.getFullYear(), 0, 0).getTime()) / (1000 * 60 * 60 * 24));
  const puzzleIndex = dayOfYear % chessPuzzles.length;
  return chessPuzzles[puzzleIndex];
};

// Get puzzles by difficulty
export const getPuzzlesByDifficulty = (difficulty: 'easy' | 'medium' | 'hard'): ChessPuzzle[] => {
  return chessPuzzles.filter(p => p.difficulty === difficulty);
};

// Get puzzles by theme
export const getPuzzlesByTheme = (theme: string): ChessPuzzle[] => {
  return chessPuzzles.filter(p => p.theme === theme);
};

// Get unique themes
export const getUniqueThemes = (): string[] => {
  return [...new Set(chessPuzzles.map(p => p.theme))];
};
